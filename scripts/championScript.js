/* ----------  RENDER MY CHAMPIONS  ---------- */
function renderChampions() {
  const ids   = JSON.parse(localStorage.getItem('owned'))?.fighters || [];
  const grid  = document.getElementById('grid');
  const none  = document.getElementById('none');

  if (!ids.length) { none.style.display = 'block'; return; }
  none.style.display = 'none';

  grid.innerHTML = ids.map(id => {
    const mon = catalog[id];
    return `
      <div class="card">
        <img src="${mon.sprite}" onerror="this.src='images/placeholder.png'">
        <div class="name">${mon.name}</div>
      </div>`;
  }).join('');
}

renderChampions();